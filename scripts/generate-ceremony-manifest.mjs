import { mkdirSync, readdirSync, writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "..");
const publicDir = path.join(root, "public");
const outDir = path.join(root, "src", "data");
const outFile = path.join(outDir, "ceremony-gallery-manifest.ts");

/** wedding1.JPG, wedding130.jpg 등 파일명에서 순번 추출 */
function weddingIndex(name) {
  const m = name.match(/^wedding(\d+)\./i);
  return m ? Number(m[1]) : Number.POSITIVE_INFINITY;
}

let files = [];
try {
  const names = readdirSync(publicDir);
  const imageExt = /\.(jpg|jpeg|png|webp|gif)$/i;
  files = names
    .filter(
      (name) =>
        /^wedding\d+\./i.test(name) &&
        imageExt.test(name) &&
        !/\.heic$/i.test(name),
    )
    .sort((a, b) => weddingIndex(a) - weddingIndex(b));
} catch {
  files = [];
}

mkdirSync(outDir, { recursive: true });

const body =
  files.length === 0
    ? "[] as const"
    : `[\n${files.map((f) => `  ${JSON.stringify(f)},`).join("\n")}\n] as const`;

const content = `// Auto-generated by scripts/generate-ceremony-manifest.mjs — do not edit.

export const CEREMONY_GALLERY_FILENAMES = ${body};
`;

writeFileSync(outFile, content, "utf8");
console.log(`[generate-ceremony-manifest] wrote ${files.length} entries → ${path.relative(root, outFile)}`);
